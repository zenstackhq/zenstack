import { describe, expect, it } from 'vitest';
import { generateFromSchema, readDoc } from '../utils';

describe('documentation plugin: SKILL.md', () => {
    it('is not generated by default', async () => {
        const tmpDir = await generateFromSchema(`
            model User {
                id String @id @default(cuid())
            }
        `);
        expect(() => readDoc(tmpDir, 'SKILL.md')).toThrow();
    });

    it('is generated when generateSkill = true', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id String @id @default(cuid())
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toBeDefined();
        expect(skill.length).toBeGreaterThan(0);
    });

    it('contains YAML frontmatter with name, description, and activation triggers', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id String @id @default(cuid())
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toMatch(/^---\n/);
        expect(skill).toContain('name:');
        expect(skill).toContain('description:');
        expect(skill).toContain('queries');
        expect(skill).toContain('forms');
    });

    it('contains a schema overview section with entity counts', async () => {
        const tmpDir = await generateFromSchema(
            `
            /// A user.
            model User {
                id    String @id @default(cuid())
                role  Role
            }
            model Post {
                id String @id @default(cuid())
            }
            enum Role {
                ADMIN
                USER
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('## Schema Overview');
        expect(skill).toContain('2 models');
        expect(skill).toContain('1 enum');
    });

    it('lists ALL entities in overview, not capped', async () => {
        const tmpDir = await generateFromSchema(
            `
            /// M1.
            model M1 { id String @id @default(cuid()) }
            /// M2.
            model M2 { id String @id @default(cuid()) }
            /// M3.
            model M3 { id String @id @default(cuid()) }
            /// M4.
            model M4 { id String @id @default(cuid()) }
            /// M5.
            model M5 { id String @id @default(cuid()) }
            /// M6.
            model M6 { id String @id @default(cuid()) }
            /// M7.
            model M7 { id String @id @default(cuid()) }
            /// M8.
            model M8 { id String @id @default(cuid()) }
            /// M9.
            model M9 { id String @id @default(cuid()) }
            /// M10.
            model M10 { id String @id @default(cuid()) }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('**M10**');
        expect(skill).not.toContain('...and');
    });

    it('includes views in overview alongside models', async () => {
        const tmpDir = await generateFromSchema(
            `
            /// A user.
            model User {
                id String @id @default(cuid())
            }
            /// A report view.
            view UserReport {
                id Int
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('**User** (Model)');
        expect(skill).toContain('**UserReport** (View)');
    });

    // --- Conventions section ---

    it('detects ID convention in Conventions section', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id String @id @default(cuid())
            }
            model Post {
                id String @id @default(cuid())
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('## Conventions');
        expect(skill).toContain('**IDs**');
        expect(skill).toContain('cuid()');
    });

    it('detects mixins in Conventions section', async () => {
        const tmpDir = await generateFromSchema(
            `
            type Timestamps {
                createdAt DateTime @default(now())
                updatedAt DateTime @updatedAt
            }
            model User with Timestamps {
                id String @id @default(cuid())
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('**Mixins**');
        expect(skill).toContain('Timestamps');
        expect(skill).toContain('`createdAt`');
        expect(skill).toContain('`updatedAt`');
    });

    it('detects computed fields in Conventions section', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id        String @id @default(cuid())
                /// Number of posts.
                postCount Int    @computed
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('**Computed fields**');
        expect(skill).toContain('User.postCount');
        expect(skill).toContain('Never set them directly');
    });

    it('uses actual FK field names in relations convention', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id    String @id @default(cuid())
                posts Post[]
            }
            model Post {
                id       String @id @default(cuid())
                author   User   @relation(fields: [authorId], references: [id])
                authorId String
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('`authorId`');
    });

    // --- Constraints section ---

    it('includes access policies under Constraints', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id   String @id @default(cuid())
                role String

                @@allow('read', true)
                @@deny('delete', role == 'ADMIN')
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('## Constraints You Must Respect');
        expect(skill).toContain('### Access Policies');
        expect(skill).toContain('**User**');
        expect(skill).toContain("allow('read', true)");
        expect(skill).toContain("deny('delete'");
    });

    it('notes auth() dependency when present', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id String @id @default(cuid())
                @@allow('update', auth() == this)
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('auth()');
        expect(skill).toContain('unauthenticated');
    });

    it('omits Constraints section when no policies or validation', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id String @id @default(cuid())
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).not.toContain('## Constraints You Must Respect');
    });

    it('includes validation under Constraints with instructional context', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id    String @id @default(cuid())
                email String @email
                name  String @length(1, 100)
                bio   String? @contains('hello')
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('### Validation');
        expect(skill).toContain('email: @email');
        expect(skill).toContain('name: @length');
        expect(skill).toContain('bio: @contains');
        expect(skill).toContain('test data');
    });

    // --- Workflow section ---

    it('includes How To Use section with workflow guidance', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id String @id @default(cuid())
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('## How To Use This Schema');
        expect(skill).toContain('### Writing queries or mutations');
        expect(skill).toContain('### Generating test data');
    });

    it('includes procedure signatures in workflow section', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id String @id @default(cuid())
            }
            enum Role { ADMIN USER }
            /// Register a new user.
            mutation procedure signUp(email: String, name: String, role: Role?): User
            /// Get user by id.
            procedure getUser(id: String): User
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('### Calling procedures');
        expect(skill).toContain('signUp');
        expect(skill).toContain('mutation');
        expect(skill).toContain('getUser');
        expect(skill).toContain('query');
        expect(skill).toContain('Register a new user.');
        expect(skill).toContain('email: String');
        expect(skill).toContain('→ User');
    });

    // --- Entity Reference: full declarations ---

    it('renders models as full prisma declaration blocks with /// comments', async () => {
        const tmpDir = await generateFromSchema(
            `
            /// A registered user.
            model User {
                id    String @id @default(cuid())
                /// User's email address.
                email String @unique
                name  String
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('## Entity Reference');
        expect(skill).toContain('### Models');
        expect(skill).toContain('#### User');
        expect(skill).toContain('```prisma');
        expect(skill).toContain('/// A registered user.');
        expect(skill).toContain('model User {');
        expect(skill).toContain("/// User's email address.");
        expect(skill).toContain('    email String @unique');
        expect(skill).toContain('}');
    });

    it('renders enums as full prisma declaration blocks with /// comments', async () => {
        const tmpDir = await generateFromSchema(
            `
            /// User roles.
            enum Role {
                /// Full access
                ADMIN
                /// Standard user
                USER
                GUEST
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('### Enums');
        expect(skill).toContain('#### Role');
        expect(skill).toContain('```prisma');
        expect(skill).toContain('/// User roles.');
        expect(skill).toContain('enum Role {');
        expect(skill).toContain('/// Full access');
        expect(skill).toContain('    ADMIN');
        expect(skill).toContain('    GUEST');
    });

    it('renders types as full prisma declaration blocks', async () => {
        const tmpDir = await generateFromSchema(
            `
            /// Shared timestamp fields.
            type Timestamps {
                createdAt DateTime @default(now())
                updatedAt DateTime @updatedAt
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('### Types');
        expect(skill).toContain('#### Timestamps');
        expect(skill).toContain('/// Shared timestamp fields.');
        expect(skill).toContain('type Timestamps {');
        expect(skill).toContain('    createdAt DateTime @default(now())');
    });

    it('renders views as full prisma declaration blocks', async () => {
        const tmpDir = await generateFromSchema(
            `
            /// User reporting view.
            view UserReport {
                id   Int
                name String
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('### Views');
        expect(skill).toContain('#### UserReport');
        expect(skill).toContain('/// User reporting view.');
        expect(skill).toContain('view UserReport {');
    });

    it('includes relationships directly under model declaration', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id    String @id @default(cuid())
                posts Post[]
            }
            model Post {
                id       String @id @default(cuid())
                author   User   @relation(fields: [authorId], references: [id])
                authorId String
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');

        expect(skill).toContain('#### User');
        expect(skill).toContain('#### Post');

        const userParts = skill.split('#### User');
        expect(userParts.length).toBeGreaterThanOrEqual(2);
        const userSection = userParts[1]!.split('####')[0]!;
        expect(userSection).toContain('Relationships:');
        expect(userSection).toContain('posts → Post (has many)');

        const postParts = skill.split('#### Post');
        expect(postParts.length).toBeGreaterThanOrEqual(2);
        const postSection = postParts[1]!.split('####')[0]!;
        expect(postSection).toContain('author → User (required)');
    });

    it('includes model-level attributes in declaration block', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id String @id @default(cuid())
                email String @unique

                @@allow('read', true)
                @@index([email])
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain("@@allow('read', true)");
        expect(skill).toContain('@@index([email])');
    });

    it('shows with clause in model declaration', async () => {
        const tmpDir = await generateFromSchema(
            `
            type Timestamps {
                createdAt DateTime @default(now())
                updatedAt DateTime @updatedAt
            }
            model User with Timestamps {
                id String @id @default(cuid())
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('model User with Timestamps {');
    });

    // --- Links with entity name and type ---

    it('uses entity name and type in link text for models', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id String @id @default(cuid())
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('[User (Model)](./models/User.md)');
        expect(skill).not.toContain('[Full documentation]');
    });

    it('uses entity name and type in link text for enums', async () => {
        const tmpDir = await generateFromSchema(
            `
            enum Role { ADMIN USER }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('[Role (Enum)](./enums/Role.md)');
    });

    it('uses entity name and type in link text for types', async () => {
        const tmpDir = await generateFromSchema(
            `
            type Timestamps {
                createdAt DateTime @default(now())
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('[Timestamps (Type)](./types/Timestamps.md)');
    });

    it('uses entity name and type in link text for views', async () => {
        const tmpDir = await generateFromSchema(
            `
            view UserReport {
                id Int
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('[UserReport (View)](./views/UserReport.md)');
    });

    it('uses entity name and type in procedure links', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id String @id @default(cuid())
            }
            procedure getUser(id: String): User
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('[getUser (Procedure)](./procedures/getUser.md)');
    });

    // --- Footer ---

    it('includes a Detailed Documentation section linking to index and relationships', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id    String @id @default(cuid())
                posts Post[]
            }
            model Post {
                id       String @id @default(cuid())
                author   User   @relation(fields: [authorId], references: [id])
                authorId String
            }
        `,
            { generateSkill: true },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('## Detailed Documentation');
        expect(skill).toContain('[Full schema index](./index.md)');
        expect(skill).toContain('[Relationships and ER diagrams](./relationships.md)');
    });

    it('uses custom title when provided', async () => {
        const tmpDir = await generateFromSchema(
            `
            model User {
                id String @id @default(cuid())
            }
        `,
            { generateSkill: true, title: 'Acme Platform' },
        );
        const skill = readDoc(tmpDir, 'SKILL.md');
        expect(skill).toContain('Acme Platform');
    });
});
